                    <h4>2. Çerez Nedir?</h4><br>
                    Çerezler bir internet sitesi tarafından cihazınızda oluşturulan ve isim-değer formatında veri
                    barındıran küçük metin dosyalarıdır. Çerezler, ziyaret ettiğiniz internet sitesinin cihazınızda
                    bilgi saklamasını ve bu bilgileri sonraki ziyaretleriniz sırasında kullanmasını mümkün
                    kılmaktadır. Bir internet sitesi tarafından oluşturulan çerezler siteye erişim için
                    kullandığınız internet tarayıcısı tarafından saklanmaktadır.
                    <br>
                    Çerezler, günümüzde, internet teknolojilerinin önemli bir parçası haline gelmiştir ve temel
                    işlevleri ziyaretçinin ve tercihlerinin hatırlanması ve cihazın tanınmasıdır. Buna bağlı olarak,
                    neredeyse bütün erişim sağladığınız internet sitesinde çerez kullanımı söz konusudur.
                    <br>
                    Bu noktada, çerezlerin tarayıcınızda saklanmasıyla ilgili olarak iki farklı tür ortaya
                    çıkmaktadır. Bunlardan ilki kalıcı çerezler olarak tanımlanmaktadır. Bu tür çerezler saklama
                    için belirlenen süre boyunca çevrimiçi ziyaretçinin cihazında saklanmakta olup, çevrimiçi
                    ziyaretçi ilgili çerezi oluşturan web sitesini her ziyaret ettiğinde etkin hale gelir.


                    <br>
