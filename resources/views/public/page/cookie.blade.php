                    <h5>Analitik/Performans</h5>
                    Bu çerezler, internet sitemizin performansını ölçmek ve iyileştirmek için ziyaretleri ve trafik
                    kaynaklarını tespit etmemize olanak sağlar. İnternet sitemiz içerisinde yer alan sayfaların
                    ziyaretçi sayılarına ulaşmamıza ve ziyaretçilerimizin internet sitemizin içerisinde hangi
                    sayfalarda zaman geçirdiklerini görmemize yardımcı olurlar. İlgili çerezlerin topladığı tüm
                    bilgiler, toplu olarak/birlikte değerlendirildiğinden kişisel bilgiler içermez, anonimdir. Bu
                    çerezler vasıtasıyla tamamen internet sitemizin daha verimli hale getirilmesi sağlanmaya
                    çalışılmaktadır.
                    <br>
                    <br>

                    <h5>İşlevsellik</h5>

                    Bu çerezler, ziyaretçilerimizin internet sitemiz üzerinde dil veya bölge seçiminin hatırlanması
                    gibi gelişmiş işlevsellik ve kişiselleştirme imkanları sağlamak amacıyla kullanılmaktadır.
                    İlgili çerezlerin kullanımına izin vermemeniz halinde, internet sitemiz nezdindeki
                    kişiselleştirilmiş ayarlarınızın kaydedilmesi mümkün olmayacaktır. BD Akademi tarafından veya
                    hizmetlerinden sayfalarımızda faydalandığımız üçüncü taraf sağlayıcılar tarafından
                    kullanılabilirler.
