                    <h4>4. Çerezlerin Kullanımına İlişkin Bilgilendirme ve Çerezlerin Yönetimi</h4>
                    <br>
                    İşbu internet sitesini kullanarak, çerez kullanımını kabul etmiş olursunuz. İnternet sitemizi
                    ilk defa ziyaret ettiğinizde ya da çerezlerinizi sıfırladığınızda, sizleri çerez kullanımımıza
                    ilişkin olarak bilgilendiririz. İnternet sitemizi kullanabilmek için çerez kullanımı zorunlu
                    değildir, fakat tarayıcınızı çerezleri kabul etmemeye ayarlamanız halinde kullanıcı
                    deneyiminizin kalitesinin düşebileceğini ve internet sitemizin çeşitli işlevlerinin
                    çalışmayabileceğini belirtmek isteriz. İnternet tarayıcılarının birçoğu tüm çerezleri kabul eder
                    şekilde geliştirilmiş olup, kullanıcılar tarayıcı ayarları değiştirerek de çerezleri
                    engelleyebilir veya belirli siteler için engelleyecek şekilde, çerez oluşturulduğunda uyarı
                    verecek şekilde veya tüm çerezleri oturum çerezi gibi sayacak şekilde yapılandırabilirsiniz. Bu
                    kapsamda, tarayıcı ayarlarınızı değiştirerek çerez kullanımını reddedebilir, çerezleri silebilir
                    veya tarayıcınızda saklanan çerezlerin listesini ve değerlerini görebilirsiniz. Tarayıcınız
                    üzerinden çerez kullanımına ilişkin ayarları değiştirmek hakkında daha detaylı bilgi için
                    aşağıda yer alan tarayıcılara özel olarak düzenlenmiş linkleri ilgili olan tarayıcıya ait logoya
                    tıklayarak inceleyebilirsiniz.

