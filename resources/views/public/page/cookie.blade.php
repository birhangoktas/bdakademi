                    tıklama ve ziyaret geçmişinizin takibini ve farklı alan adları arasında bu kayıtların
                    eşlenmesini mümkün kılmaktadır. Bu tür çerezler kullanıcıların tanınması ve profillenmesi,
                    reklam ve pazarlama faaliyetlerinin hedeflenmesi ve içeriğin özelleştirilmesi amaçlarıyla
